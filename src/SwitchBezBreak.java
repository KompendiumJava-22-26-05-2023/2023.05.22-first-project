public class SwitchBezBreak {
    public static void main(String[] args) {
        int k = 3;
        switch (k) {
            case 1:
                System.out.println("k1");
            case 2:
                System.out.println("k2");
            case 3:
                System.out.println("k3");
            case 4:
                System.out.println("k4");
            case 5:
                System.out.println("k5");
        }
    }
}
